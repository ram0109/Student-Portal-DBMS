CREATE DATABASE StudentPortal;
use StudentPortal

CREATE TABLE LOGIN_INFO
( USERID VARCHAR(40) PRIMARY KEY NOT NULL,
PASSWORD_ VARCHAR(30) NOT NULL
);
CREATE TABLE STUDENT (
    USERID VARCHAR(40) NOT NULL,   -- Changed to VARCHAR
    S_NAME VARCHAR(40) NOT NULL,   -- Changed to VARCHAR
    ROLL_NUMBER INT PRIMARY KEY NOT NULL,
    UG_PG VARCHAR(2) NOT NULL,
    FOREIGN KEY (USERID) REFERENCES LOGIN_INFO(USERID)
);
CREATE TABLE FEES (
    ROLL_NUMBER INT NOT NULL,  -- Use INT instead of NUMBER for MySQL
    TUTION_FEE DECIMAL(10, 2) NOT NULL,  -- Using DECIMAL for financial values
    HOSTEL_FEE DECIMAL(10, 2) NOT NULL,
    OTHER_CHARGES DECIMAL(10, 2) NOT NULL,
    PRIMARY KEY (ROLL_NUMBER),  -- Set the primary key
    FOREIGN KEY (ROLL_NUMBER) REFERENCES STUDENT(ROLL_NUMBER)  -- Foreign key reference
);
-- SUBJECT Table
CREATE TABLE SUBJECT (
    SUBJECT_NAME VARCHAR(40) PRIMARY KEY NOT NULL,
    RECORDINGS VARCHAR(100) NOT NULL,
    READING_MATERIAL VARCHAR(100) NOT NULL,
    CREDITS SMALLINT NOT NULL
);

-- EXAM Table
CREATE TABLE EXAM (
    SUBJECT VARCHAR(40) NOT NULL,
    EXAM_CODE VARCHAR(6) PRIMARY KEY NOT NULL,
    MARKS DECIMAL(5, 2) NOT NULL,  -- Assuming marks can have decimals
    EXAM_DATE DATE NOT NULL,
    LENGTH VARCHAR(7) NOT NULL,
    TYPE_ VARCHAR(7) NOT NULL,  -- Renamed to TYPE_ to avoid using reserved keyword
    FOREIGN KEY (SUBJECT) REFERENCES SUBJECT(SUBJECT_NAME)
);

-- COURSE Table
CREATE TABLE COURSE (
    ROLL_NUMBER INT NOT NULL,  -- Replaced NUMBER with INT for MySQL
    SUBJECT_NAME VARCHAR(40) NOT NULL,
    PRIMARY KEY(SUBJECT_NAME, ROLL_NUMBER),
    FOREIGN KEY (ROLL_NUMBER) REFERENCES STUDENT(ROLL_NUMBER),
    FOREIGN KEY (SUBJECT_NAME) REFERENCES SUBJECT(SUBJECT_NAME)
);

-- PROJECTS Table
CREATE TABLE PROJECTS (
    PROJECT_NAME VARCHAR(40) PRIMARY KEY NOT NULL,
    SUBJECT VARCHAR(40) NOT NULL,
    YEAR_ INT NOT NULL
);

-- MADE Table
CREATE TABLE MADE (
    ROLL_NUMBER INT NOT NULL,
    PROJECT_NAME VARCHAR(40) NOT NULL,
    FOREIGN KEY (ROLL_NUMBER) REFERENCES STUDENT(ROLL_NUMBER),
    FOREIGN KEY (PROJECT_NAME) REFERENCES PROJECTS(PROJECT_NAME)
);

-- FACULTY Table
CREATE TABLE FACULTY (
    FAC_ID INT PRIMARY KEY NOT NULL,
    FAC_NAME VARCHAR(20) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL,
    SUBJECT_NAME VARCHAR(40) NOT NULL,
    FOREIGN KEY (SUBJECT_NAME) REFERENCES SUBJECT(SUBJECT_NAME)
);

-- ATTENDANCE Table
CREATE TABLE ATTENDANCE (
    ROLL_NUMBER INT NOT NULL,
    FAC_ID INT NOT NULL,
    PERCENTAGE DECIMAL(5, 2) NOT NULL,  -- Using DECIMAL for percentage precision
    FOREIGN KEY (ROLL_NUMBER) REFERENCES STUDENT(ROLL_NUMBER),
    FOREIGN KEY (FAC_ID) REFERENCES FACULTY(FAC_ID)
);

CREATE TABLE ASSIGNMENT (
    SUBJECT_NAME VARCHAR(100) NOT NULL,
    ASSIGNMENT_NAME VARCHAR(100) NOT NULL,
    LAB_THEORY VARCHAR(20) NOT NULL,
    DEADLINE DATE NOT NULL,
    PRIMARY KEY (SUBJECT_NAME, ASSIGNMENT_NAME)
);

