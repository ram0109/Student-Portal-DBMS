-- 4.The minimum attendance percentage a student must maintain to be able to appear for the exams
-- is 80%. List the names of the students along with their attendance percentages who are NOT
-- eligible for exam. Also mention the corresponding faculty name and the subject name.

SELECT S.ROLL_NUMBER, S_NAME, PERCENTAGE, FAC_NAME, SUBJECT_NAME
FROM ((STUDENT S INNER JOIN ATTENDANCE A ON S.ROLL_NUMBER=A.ROLL_NUMBER)
INNER JOIN FACULTY F ON A.FAC_ID= F.FAC_ID)
WHERE PERCENTAGE<80;

-- 5. Display the course that was chosen by maximum number of students
SELECT SUBJECT_NAME, COUNT(*) AS NUMBER_OF_TIMES 
FROM COURSE 
GROUP BY SUBJECT_NAME 
HAVING COUNT(*) = (
    SELECT MAX(Cnt) 
    FROM (
        SELECT COUNT(*) AS Cnt 
        FROM COURSE 
        GROUP BY SUBJECT_NAME
    ) AS CountTable
);

